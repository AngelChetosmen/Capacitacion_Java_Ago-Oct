package entradas.flujo;

public class Condicional {
    public static void main(String[] args) {
        int edad = 19; 
        if (edad >= 18) {
            System.out.println("Persona mayor de edad: " + edad);
        }else{
            System.out.println("Persona menor de edad: " + edad);
        }
        System.out.println("Continua el Flujo del programa");
    }
}
